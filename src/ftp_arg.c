#include "ft_p.h"

int		arg(char *cmd)
{
	int		i;

	i = 0;
	while (cmd[i] && !ft_isspace(cmd[i]))
		i++;
	while (cmd[i] && ft_isspace(cmd[i]))
		i++;
	return (i);
}
